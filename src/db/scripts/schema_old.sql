from tasks t left join task_employee te on te.id_task=t.id 
inner join employees emp on emp.id_employees = t.id_creator
WHERE  te.id_employee= i or t.id_creator = i
Group BY t.id
HAVING datetime_of_creation >= NOW() - INTERVAL 30 MINUTE and datetime_of_creation > last_notif
Order by t.id DESC;
